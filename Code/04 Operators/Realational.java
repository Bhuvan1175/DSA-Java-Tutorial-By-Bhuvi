public class Realational {
    public static void main(String[] args) {
        int A=10;
        int B=20;
        System.out.println("Equals : "+(A==B));
        System.out.println("Not Equals : "+(A!=B));
        System.out.println("Greater : "+(A>B));
        System.out.println("Smaller : "+(A<B));
        System.out.println("Greater Equal : "+(A>=B));
        System.out.println("Smaller Equal : "+(A<=B));
    }
}
